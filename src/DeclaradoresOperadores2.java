public class DeclaradoresOperadores2 {
    public static void main(String[] args){
        final double apulgadas=2.54;
        double cm =6;
        double resultado=cm/apulgadas;

        int operador1, operador2, resultado_operacion; // Declaramos tres variables en la misma linea
        operador1=8;
        operador2=7;
        resultado_operacion=operador1+operador2;



        System.out.println("En "+ cm + " cm, hay "+ resultado+ " pulgadas");
        System.out.println(resultado_operacion);

    }
}
