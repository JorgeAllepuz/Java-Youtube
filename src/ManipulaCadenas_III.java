public class ManipulaCadenas_III {
    public static void main(String[] args) {
        String alumno1, alumno2;
        alumno1="David";
        alumno2="David";

        System.out.println(alumno1.equals(alumno2)); // equals determina si dos variables son iguales
        System.out.println(alumno1.equalsIgnoreCase(alumno2)); // equalsIgnoreCase determina si dos variables son iguales ignorando mayusculas y minusculas
    }
}
